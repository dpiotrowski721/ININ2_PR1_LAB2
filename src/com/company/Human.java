package com.company;

public class Human {

    String firstName;
    String lastName;
    Integer age;
    Double salary;
    Animal pet;
    Car car;


    Human(String firstName, String lastName, Integer age, Double salary, Animal pet, Car car){
        this.firstName = firstName;
        this.lastName = lastName;
        this.age = age;
        this.salary = salary;
        this.pet = pet;
        this.car = car;
    }


}
